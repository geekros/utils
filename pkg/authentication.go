package utils

import (
	"time"

	"github.com/golang-jwt/jwt/v4"
)

func GenerateToken(secret string, data map[string]interface{}, expiration int) (string, error) {
	claims := jwt.MapClaims{}

	for k, v := range data {
		claims[k] = v
	}

	claims["exp"] = time.Now().Add(time.Hour * time.Duration(expiration)).Unix()

	token := jwt.NewWithClaims(jwt.SigningMethodHS256, claims)

	tokenString, err := token.SignedString([]byte(secret))
	if err != nil {
		return "", err
	}

	return tokenString, nil
}

func ValidateToken(secret string, tokenStr string) (map[string]interface{}, error) {
	token, err := jwt.Parse(tokenStr, func(token *jwt.Token) (interface{}, error) {
		return []byte(secret), nil
	})

	if err != nil {
		return nil, err
	}

	if claims, ok := token.Claims.(jwt.MapClaims); ok && token.Valid {
		return claims, nil
	} else {
		return nil, err
	}
}
